def plot_confusion_matrix(y_true, y_pred, title="Confusion Matrix", ax=None):
    """Plot confusion matrix heatmap."""
    cm = confusion_matrix(y_true, y_pred)
    if ax is None:
        fig, ax = plt.subplots(figsize=(6, 4))
    im = ax.imshow(cm, cmap="Blues")
    n_classes = cm.shape[0]
    ax.set_xticks(range(n_classes))
    ax.set_yticks(range(n_classes))
    for i in range(n_classes):
        for j in range(n_classes):
            ax.text(j, i, str(cm[i, j]), ha="center", va="center", fontsize=14)
    plt.colorbar(im, ax=ax)
    ax.set_title(title)
    return ax


def plot_learning_curve(
    model, X, y, train_sizes=None, cv=5, scoring="neg_mean_squared_error"
):
    """
    Plot learning curve: train and val score vs training set size.
    """
    from sklearn.model_selection import learning_curve
    if train_sizes is None:
        train_sizes = np.linspace(0.2, 1.0, 8)
    train_sz, train_scores, val_scores = learning_curve(
        model, X, y, train_sizes=train_sizes, cv=cv, scoring=scoring
    )
    if "neg_" in scoring:
        train_scores, val_scores = -train_scores, -val_scores
    fig, ax = plt.subplots(figsize=(8, 5))
    ax.plot(train_sz, train_scores.mean(axis=1), "b-o", label="Train")
    ax.fill_between(
        train_sz,
        train_scores.mean(axis=1) - train_scores.std(axis=1),
        train_scores.mean(axis=1) + train_scores.std(axis=1),
        alpha=0.2,
        color="blue",
    )
    ax.plot(train_sz, val_scores.mean(axis=1), "r-s", label="Validation")
    ax.fill_between(
        train_sz,
        val_scores.mean(axis=1) - val_scores.std(axis=1),
        val_scores.mean(axis=1) + val_scores.std(axis=1),
        alpha=0.2,
        color="red",
    )
    ax.set_xlabel("Training set size")
    ax.set_ylabel("Score (lower is better for MSE)")
    ax.set_title("Learning Curve")
    ax.legend()
    ax.grid(True, alpha=0.3)
    plt.tight_layout()
    return ax


def plot_feature_importance(model, feature_names, ax=None):
    """Plot feature importance (for tree-based models)."""
    imp = model.feature_importances_
    idx = np.argsort(imp)
    if ax is None:
        fig, ax = plt.subplots(figsize=(8, max(4, len(feature_names) * 0.3)))
    ax.barh([feature_names[i] for i in idx], imp[idx], color="steelblue")
    ax.set_xlabel("Importance")
    ax.set_title("Feature Importance")
    plt.tight_layout()
    return ax


def compare_models(X_train, y_train, X_test, y_test, models, task="classification"):
    """
    Train and compare multiple models. models: dict of name -> model instance.
    Returns DataFrame of metrics.
    """
    results = []
    for name, model in models.items():
        model.fit(X_train, y_train)
        metrics = evaluate_model(model, X_test, y_test, task=task)
        metrics["model"] = name
        results.append(metrics)
    return pd.DataFrame(results)


