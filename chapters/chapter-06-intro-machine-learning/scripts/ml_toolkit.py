def train_model(X_train, y_train, model_type="linear", **kwargs):
    """Train a model. model_type: 'linear', 'logistic', 'random_forest'."""
    if model_type == "linear":
        model = LinearRegression(**kwargs)
    elif model_type == "logistic":
        model = LogisticRegression(max_iter=1000, **kwargs)
    elif model_type == "random_forest":
        model = RandomForestClassifier(**kwargs)
    else:
        raise ValueError(f"Unknown model_type: {model_type}")
    model.fit(X_train, y_train)
    return model


def evaluate_model(model, X_test, y_test, task="regression"):
    """
    Evaluate model. task: 'regression' or 'classification'.
    Returns dict of metrics.
    """
    y_pred = model.predict(X_test)
    if task == "regression":
        return {
            "mse": mean_squared_error(y_test, y_pred),
            "rmse": np.sqrt(mean_squared_error(y_test, y_pred)),
        }
    else:
        return {
            "accuracy": accuracy_score(y_test, y_pred),
            "precision": precision_score(y_test, y_pred, zero_division=0),
            "recall": recall_score(y_test, y_pred, zero_division=0),
            "f1": f1_score(y_test, y_pred, zero_division=0),
        }


def cross_validate(X, y, model, cv=5, scoring="neg_mean_squared_error"):
    """Run k-fold cross-validation. Returns mean and std of scores."""
    kf = KFold(n_splits=cv, shuffle=True, random_state=42)
    scores = cross_val_score(model, X, y, cv=kf, scoring=scoring)
    if "neg_" in scoring:
        scores = -scores
    return scores.mean(), scores.std()


