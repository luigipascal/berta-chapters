# Gradient descent for linear regression
# -----------------------------------------------------------------------------

def gradient_descent(X, y, learning_rate=0.01, epochs=1000):
    """Gradient descent for linear regression y ≈ Xw + b.

    Args:
        X: List of lists (samples x features) or flat list (single feature).
        y: Target values.
        learning_rate: Step size.
        epochs: Number of iterations.

    Returns:
        Tuple of (w, b, history) where history is list of MSE per epoch.
    """
    try:
        import numpy as np
    except ImportError:
        raise ImportError("NumPy required for gradient_descent")

    X = np.array(X)
    if X.ndim == 1:
        X = X.reshape(-1, 1)
    y = np.array(y).reshape(-1, 1)
    n, d = X.shape

    w = np.zeros((d, 1))
    b = 0.0
    history = []

    for _ in range(epochs):
        pred = X @ w + b
        error = pred - y
        dw = (2 / n) * (X.T @ error)
        db = (2 / n) * np.sum(error)
        w -= learning_rate * dw
        b -= learning_rate * db
        mse = float(np.mean(error ** 2))
        history.append(mse)

    return w.flatten().tolist(), float(b), history


