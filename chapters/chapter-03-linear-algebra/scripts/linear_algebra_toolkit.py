import math


# -----------------------------------------------------------------------------
# Vector operations
# -----------------------------------------------------------------------------

def vector_add(u, v):
    """Add two vectors element-wise."""
    if len(u) != len(v):
        raise ValueError("Vectors must have same dimension")
    return [a + b for a, b in zip(u, v)]


def vector_subtract(u, v):
    """Subtract vectors: u - v."""
    if len(u) != len(v):
        raise ValueError("Vectors must have same dimension")
    return [a - b for a, b in zip(u, v)]


def vector_scale(c, u):
    """Multiply vector by scalar."""
    return [c * x for x in u]


def vector_dot(u, v):
    """Dot product: sum of element-wise products."""
    if len(u) != len(v):
        raise ValueError("Vectors must have same dimension")
    return sum(a * b for a, b in zip(u, v))


def vector_norm(u, p=2):
    """Compute Lp norm. p=1 for L1, p=2 for L2 (default)."""
    if p == 1:
        return sum(abs(x) for x in u)
    if p == 2:
        return math.sqrt(sum(x * x for x in u))
    return (sum(abs(x) ** p for x in u)) ** (1 / p)


def cosine_similarity(u, v):
    """Cosine similarity: dot product normalized by L2 norms."""
    dot = vector_dot(u, v)
    norm_u = vector_norm(u, 2)
    norm_v = vector_norm(v, 2)
    if norm_u == 0 or norm_v == 0:
        return 0.0
    return dot / (norm_u * norm_v)


# -----------------------------------------------------------------------------
# Matrix operations
# -----------------------------------------------------------------------------

def matrix_transpose(A):
    """Transpose: rows become columns."""
    if not A:
        return []
    n_rows, n_cols = len(A), len(A[0])
    return [[A[i][j] for i in range(n_rows)] for j in range(n_cols)]


def matrix_multiply(A, B):
    """Matrix multiply: A (m×k) @ B (k×n) -> (m×n)."""
    m, k1 = len(A), len(A[0])
    k2, n = len(B), len(B[0])
    if k1 != k2:
        raise ValueError(f"Inner dims must match: {k1} vs {k2}")
    result = [[0] * n for _ in range(m)]
    for i in range(m):
        for j in range(n):
            result[i][j] = sum(A[i][kk] * B[kk][j] for kk in range(k1))
    return result


def matrix_vector_multiply(A, x):
    """Matrix (m×n) times vector (n) -> vector (m)."""
    return [sum(A[i][j] * x[j] for j in range(len(x))) for i in range(len(A))]


