# Linear Regression From Scratch
# =============================================================================

class LinearRegressionScratch:
    """Linear regression using normal equation or gradient descent."""

    def __init__(self, method="normal", learning_rate=0.01, epochs=1000):
        self.method = method
        self.learning_rate = learning_rate
        self.epochs = epochs
        self.weights = None
        self.intercept = None
        self.history = []

    def fit(self, X, y):
        X = np.asarray(X)
        y = np.asarray(y).ravel()
        if X.ndim == 1:
            X = X.reshape(-1, 1)
        n_samples, n_features = X.shape
        X_b = np.c_[np.ones((n_samples, 1)), X]

        if self.method == "normal":
            self.weights = np.linalg.lstsq(X_b.T @ X_b, X_b.T @ y, rcond=None)[0]
            self.intercept = self.weights[0]
            self.weights = self.weights[1:]
        else:
            self.weights = np.zeros(n_features)
            self.intercept = 0.0
            for _ in range(self.epochs):
                pred = X_b @ np.concatenate([[self.intercept], self.weights])
                errors = pred - y
                grad = X_b.T @ errors / n_samples
                self.intercept -= self.learning_rate * grad[0]
                self.weights -= self.learning_rate * grad[1:]
                mse = np.mean(errors ** 2)
                self.history.append(mse)
        return self

    def predict(self, X):
        X = np.asarray(X)
        if X.ndim == 1:
            X = X.reshape(-1, 1)
        return X @ self.weights + self.intercept

    def score(self, X, y):
        y_pred = self.predict(X)
        ss_res = np.sum((y - y_pred) ** 2)
        ss_tot = np.sum((y - np.mean(y)) ** 2)
        if ss_tot == 0:
            return 0.0
        return 1 - ss_res / ss_tot


