def normalize_and_clip(
    data: List[float],
    mean: float,
    std: float,
    low: float = -3.0,
    high: float = 3.0,
) -> List[float]:
    if not data:
        return []
    if std == 0:
        return [0.0] * len(data)
    normalized = [(x - mean) / std for x in data]
    return [max(low, min(high, x)) for x in normalized]


def test_normalize_basic():
    data = [1.0, 2.0, 3.0]
    mean, std = 2.0, 1.0
    result = normalize_and_clip(data, mean, std, -10, 10)
    assert result == [-1.0, 0.0, 1.0]


def test_clip_extreme_values():
    data = [0.0]  # after norm with mean=0, std=1 -> [0]
    result = normalize_and_clip(data, 0, 1, low=-1, high=1)
    assert result == [0.0]
    data_extreme = [-10.0, 10.0]  # normalizing with mean=0, std=1
    result2 = normalize_and_clip(data_extreme, 0, 1, low=-2, high=2)
    assert result2 == [-2.0, 2.0]  # clipped


def test_empty_input():
    assert normalize_and_clip([], 0, 1) == []


# Exercise 4: Config System
class Config:
    def __init__(self, learning_rate: float, epochs: int, model_name: str):
        self.learning_rate = learning_rate
        self.epochs = epochs
        self.model_name = model_name

    def validate(self) -> None:
        if self.learning_rate <= 0:
            raise ValueError("learning_rate must be positive")
        if self.epochs < 1:
            raise ValueError("epochs must be >= 1")
        if not self.model_name or not isinstance(self.model_name, str):
            raise ValueError("model_name must be non-empty string")

    @classmethod
    def from_dict(cls, d: Dict[str, Any]) -> "Config":
        return cls(
            learning_rate=float(d["learning_rate"]),
            epochs=int(d["epochs"]),
            model_name=str(d["model_name"]),
        )


