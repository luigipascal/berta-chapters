class DataLoader:
    def __init__(self, data: List[List[float]], target_idx: int = -1):
        self.X = [row[:target_idx] for row in data]
        self.y = [row[target_idx] for row in data]

    def get_xy(self):
        return self.X, self.y


class SimpleModel:
    def __init__(self):
        self.weights = []
        self.bias = 0.0
        self.in_features = 0

    def fit(self, X: List[List[float]], y: List[float], epochs: int = 100, lr: float = 0.01):
        if not X:
            return
        self.in_features = len(X[0])
        self.weights = [0.0] * self.in_features
        self.bias = 0.0
        n = len(X)
        for _ in range(epochs):
            preds = [sum(w * xi for w, xi in zip(self.weights, row)) + self.bias for row in X]
            errors = [p - t for p, t in zip(preds, y)]
            for j in range(self.in_features):
                grad_j = (2 / n) * sum(e * row[j] for e, row in zip(errors, X))
                self.weights[j] -= lr * grad_j
            self.bias -= lr * (2 / n) * sum(errors)

    def predict(self, X: List[List[float]]) -> List[float]:
        return [
            sum(w * xi for w, xi in zip(self.weights, row)) + self.bias
            for row in X
        ]


class Trainer:
    def __init__(self, config: Dict[str, Any]):
        self.epochs = config.get("epochs", 100)
        self.lr = config.get("learning_rate", 0.01)

    def fit(self, model: SimpleModel, data_loader: DataLoader) -> None:
        X, y = data_loader.get_xy()
        model.fit(X, y, epochs=self.epochs, lr=self.lr)


def run_experiment(config: Dict[str, Any], data: List[List[float]]) -> SimpleModel:
    data_loader = DataLoader(data)
    model = SimpleModel()
    trainer = Trainer(config)
    trainer.fit(model, data_loader)
    return model


