from abc import ABC, abstractmethod


# Exercise 1: Refactor Spaghetti ML Code
def load_data(data: List[List[float]]) -> tuple:
    """Extract features (X) and targets (y). Assume last column is target."""
    X = [row[:-1] for row in data]
    y = [row[-1] for row in data]
    return X, y


def compute_gradient_simple(X: List[float], y: List[float], w: float, b: float) -> tuple:
    """Compute grad_w and grad_b for MSE (single feature)."""
    n = len(X)
    preds = [w * xi + b for xi in X]
    errors = [p - t for p, t in zip(preds, y)]
    grad_w = (2 / n) * sum(e * x for e, x in zip(errors, X))
    grad_b = (2 / n) * sum(errors)
    return grad_w, grad_b


def train_step_simple(X: List[float], y: List[float], w: float, b: float, lr: float) -> tuple:
    """One gradient descent step. Returns (new_w, new_b)."""
    grad_w, grad_b = compute_gradient_simple(X, y, w, b)
    return w - lr * grad_w, b - lr * grad_b


def evaluate_mse(X: List[float], y: List[float], w: float, b: float) -> float:
    """MSE for y = w*x + b."""
    preds = [w * x + b for x in X]
    n = len(X)
    return sum((p - t) ** 2 for p, t in zip(preds, y)) / n


def refactored_training_pipeline(data: List[List[float]], epochs: int = 500, lr: float = 0.05):
    """Refactored: clean functions, single feature (last col = target)."""
    X_full, y = load_data(data)
    X = [row[0] for row in X_full]  # Single feature
    w, b = 0.0, 0.0
    for _ in range(epochs):
        w, b = train_step_simple(X, y, w, b, lr)
    mse = evaluate_mse(X, y, w, b)
    return w, b, mse


# Exercise 2: Strategy Pattern
class ModelStrategy(ABC):
    @abstractmethod
    def predict(self, X: List[float]) -> List[float]:
        pass


class LinearStrategy(ModelStrategy):
    def __init__(self, w: float = 1.0, b: float = 0.0):
        self.w = w
        self.b = b

    def predict(self, X: List[float]) -> List[float]:
        return [self.w * x + self.b for x in X]


class NullStrategy(ModelStrategy):
    def predict(self, X: List[float]) -> List[float]:
        return [0.0] * len(X)


class Predictor:
    def __init__(self, strategy: ModelStrategy):
        self.strategy = strategy

    def predict(self, X: List[float]) -> List[float]:
        return self.strategy.predict(X)


