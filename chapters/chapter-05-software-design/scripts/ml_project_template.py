def run_pipeline(config: Optional[Config] = None) -> Tuple[LinearModel, float]:
    """Run full pipeline: config -> data -> model -> train -> evaluate."""
    config = config or Config()
    config.validate()

    # Synthetic data: y ≈ 2*x1 + 3*x2
    data = DataLoader.from_rows([
        [1, 2, 8.1],
        [2, 3, 13.1],
        [3, 1, 9.2],
        [4, 4, 20.2],
    ], target_idx=-1)

    model = LinearModel(in_features=2)
    trainer = Trainer(model, config)
    trainer.fit(data)

    X, y = data.get_xy()
    preds = model.predict(X)
    mse = Evaluator.mse(preds, y)
    logger.info("Final MSE: %.6f", mse)
    return model, mse


if __name__ == "__main__":
    model, mse = run_pipeline()
