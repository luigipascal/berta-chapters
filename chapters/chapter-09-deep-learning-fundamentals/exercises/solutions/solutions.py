def apply_dropout(A, keep_prob=0.8, seed=None):
    if seed is not None:
        np.random.seed(seed)
    mask = (np.random.rand(*A.shape) < keep_prob).astype(float)
    A_dropped = A * mask / keep_prob
    return A_dropped, mask


