class _SimpleCNN(nn.Module):
    def __init__(self):
        super().__init__()
        self.features = nn.Sequential(
            nn.Conv2d(1, 8, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2),
        )
        self.classifier = nn.Sequential(
            nn.Flatten(),
            nn.Linear(8 * 4 * 4, 32),
            nn.ReLU(),
            nn.Linear(32, 2),
        )

    def forward(self, x):
        x = self.features(x)
        return self.classifier(x)


def build_cnn_pipeline(n_samples=500, n_epochs=5, seed=42):
    torch.manual_seed(seed)
    np.random.seed(seed)

    X = np.random.randn(n_samples, 1, 8, 8).astype(np.float32)
    y = np.random.randint(0, 2, size=n_samples)
    X[y == 1] += 0.5

    X_t = torch.tensor(X)
    y_t = torch.tensor(y, dtype=torch.long)

    model = _SimpleCNN()
    criterion = nn.CrossEntropyLoss()
    optimizer = optim.Adam(model.parameters(), lr=1e-3)

    model.train()
    for epoch in range(n_epochs):
        optimizer.zero_grad()
        logits = model(X_t)
        loss = criterion(logits, y_t)
        loss.backward()
        optimizer.step()

    model.eval()
    with torch.no_grad():
        preds = model(X_t).argmax(dim=1)
        accuracy = (preds == y_t).float().mean().item()

    return model, accuracy


