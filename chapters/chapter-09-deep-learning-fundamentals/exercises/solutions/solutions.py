class NeuralNet2Layer:
    """2-layer network (one hidden) with sigmoid activations and BCE loss."""

    def __init__(self, n_input, n_hidden, n_output=1, lr=0.1, seed=42):
        self.lr = lr
        rng = np.random.RandomState(seed)
        self.W1 = rng.randn(n_input, n_hidden) * 0.5
        self.b1 = np.zeros((1, n_hidden))
        self.W2 = rng.randn(n_hidden, n_output) * 0.5
        self.b2 = np.zeros((1, n_output))

    @staticmethod
    def _sigmoid(z):
        return 1.0 / (1.0 + np.exp(-np.clip(z, -500, 500)))

    def forward(self, X):
        self.Z1 = X @ self.W1 + self.b1
        self.A1 = self._sigmoid(self.Z1)
        self.Z2 = self.A1 @ self.W2 + self.b2
        self.A2 = self._sigmoid(self.Z2)
        return self.A2

    def backward(self, X, y):
        m = X.shape[0]
        dZ2 = self.A2 - y
        dW2 = self.A1.T @ dZ2 / m
        db2 = np.mean(dZ2, axis=0, keepdims=True)

        dA1 = dZ2 @ self.W2.T
        dZ1 = dA1 * self.A1 * (1 - self.A1)
        dW1 = X.T @ dZ1 / m
        db1 = np.mean(dZ1, axis=0, keepdims=True)

        self.W2 -= self.lr * dW2
        self.b2 -= self.lr * db2
        self.W1 -= self.lr * dW1
        self.b1 -= self.lr * db1

    def train(self, X, y, epochs=2000, verbose=True):
        y = y.reshape(-1, 1) if y.ndim == 1 else y
        losses = []
        for epoch in range(1, epochs + 1):
            y_hat = self.forward(X)
            eps = 1e-8
            loss = -np.mean(y * np.log(y_hat + eps) + (1 - y) * np.log(1 - y_hat + eps))
            losses.append(loss)
            self.backward(X, y)
            if verbose and epoch % max(1, epochs // 10) == 0:
                print(f"Epoch {epoch:>5d}/{epochs}  Loss: {loss:.6f}")
        return losses


