# Neural Network from Scratch (NumPy only)
# ---------------------------------------------------------------------------

class NeuralNetScratch:
    """Fully-connected neural network built with pure NumPy."""

    _activations = {
        "relu": (relu, relu_derivative),
        "sigmoid": (sigmoid, sigmoid_derivative),
        "tanh": (tanh, tanh_derivative),
    }

    def __init__(self, layer_sizes, activation="relu", seed=42):
        """
        Parameters
        ----------
        layer_sizes : list[int]
            Number of neurons in each layer, e.g. [2, 8, 4, 1].
        activation : str
            Hidden-layer activation: 'relu', 'sigmoid', or 'tanh'.
        seed : int
            Random seed for reproducibility.
        """
        self.layer_sizes = layer_sizes
        self.activation_name = activation
        self.act_fn, self.act_deriv = self._activations[activation]
        self.num_layers = len(layer_sizes) - 1

        rng = np.random.RandomState(seed)
        self.weights = []
        self.biases = []
        for i in range(self.num_layers):
            fan_in, fan_out = layer_sizes[i], layer_sizes[i + 1]
            scale = np.sqrt(2.0 / fan_in)
            self.weights.append(rng.randn(fan_in, fan_out) * scale)
            self.biases.append(np.zeros((1, fan_out)))

        self.A_cache = []
        self.Z_cache = []
        self.loss_history = []

    def forward(self, X):
        """Forward pass — stores all activations (A) and pre-activations (Z)."""
        self.A_cache = [X]
        self.Z_cache = []
        A = X
        for i in range(self.num_layers):
            Z = A @ self.weights[i] + self.biases[i]
            self.Z_cache.append(Z)
            if i == self.num_layers - 1:
                A = sigmoid(Z)  # output layer always sigmoid
            else:
                A = self.act_fn(Z)
            self.A_cache.append(A)
        return A

    def _compute_loss(self, y_hat, y):
        m = y.shape[0]
        eps = 1e-8
        y_hat = np.clip(y_hat, eps, 1 - eps)
        return -np.mean(y * np.log(y_hat) + (1 - y) * np.log(1 - y_hat))

    def backward(self, X, y, lr=0.01):
        """Backpropagation with chain rule; updates weights in-place."""
        m = X.shape[0]
        y = y.reshape(-1, 1) if y.ndim == 1 else y

        dA = self.A_cache[-1] - y  # derivative of BCE + sigmoid
        for i in reversed(range(self.num_layers)):
            if i == self.num_layers - 1:
                dZ = dA
            else:
                dZ = dA * self.act_deriv(self.Z_cache[i])

            dW = self.A_cache[i].T @ dZ / m
            db = np.mean(dZ, axis=0, keepdims=True)
            dA = dZ @ self.weights[i].T

            self.weights[i] -= lr * dW
            self.biases[i] -= lr * db

    def train(self, X, y, epochs=1000, lr=0.01, batch_size=None, verbose=True):
        """
        Training loop with optional mini-batch support.

        Returns
        -------
        list[float]
            Loss at each epoch.
        """
        y_col = y.reshape(-1, 1) if y.ndim == 1 else y
        m = X.shape[0]
        self.loss_history = []

        for epoch in range(1, epochs + 1):
            if batch_size is None:
                y_hat = self.forward(X)
                self.backward(X, y_col, lr)
                loss = self._compute_loss(y_hat, y_col)
            else:
                indices = np.random.permutation(m)
                epoch_loss = 0.0
                n_batches = 0
                for start in range(0, m, batch_size):
                    end = min(start + batch_size, m)
                    idx = indices[start:end]
                    Xb, yb = X[idx], y_col[idx]
                    y_hat = self.forward(Xb)
                    self.backward(Xb, yb, lr)
                    epoch_loss += self._compute_loss(y_hat, yb)
                    n_batches += 1
                loss = epoch_loss / n_batches

            self.loss_history.append(loss)
            if verbose and epoch % max(1, epochs // 10) == 0:
                print(f"Epoch {epoch:>5d}/{epochs}  Loss: {loss:.6f}")

        return self.loss_history

    def predict(self, X):
        """Forward pass + threshold at 0.5 for binary classification."""
        probs = self.forward(X)
        return (probs >= 0.5).astype(int)


