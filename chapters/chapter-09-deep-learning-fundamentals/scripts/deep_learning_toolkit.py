def plot_loss_curve(history, title="Training Loss"):
    """Plot training loss over epochs."""
    fig, ax = plt.subplots(figsize=(8, 4))
    ax.plot(history, linewidth=2, color="#2563eb")
    ax.set_xlabel("Epoch")
    ax.set_ylabel("Loss")
    ax.set_title(title)
    ax.grid(True, alpha=0.3)
    fig.tight_layout()
    return fig


def plot_decision_boundary_nn(model, X, y, title="Decision Boundary", resolution=200):
    """Visualise the decision surface of a NeuralNetScratch model."""
    x_min, x_max = X[:, 0].min() - 0.5, X[:, 0].max() + 0.5
    y_min, y_max = X[:, 1].min() - 0.5, X[:, 1].max() + 0.5
    xx, yy = np.meshgrid(
        np.linspace(x_min, x_max, resolution),
        np.linspace(y_min, y_max, resolution),
    )
    grid = np.c_[xx.ravel(), yy.ravel()]
    preds = model.predict(grid).reshape(xx.shape)

    fig, ax = plt.subplots(figsize=(7, 6))
    ax.contourf(xx, yy, preds, levels=[0, 0.5, 1], alpha=0.3, colors=["#93c5fd", "#fca5a5"])
    ax.scatter(X[:, 0], X[:, 1], c=y.ravel(), cmap="bwr", edgecolors="k", s=20, linewidths=0.5)
    ax.set_title(title)
    ax.set_xlabel("x₁")
    ax.set_ylabel("x₂")
    fig.tight_layout()
    return fig


