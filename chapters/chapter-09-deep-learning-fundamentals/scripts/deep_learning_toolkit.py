def plot_network_architecture(layer_sizes, title="Network Architecture"):
    """Draw a simple network diagram with circles and lines."""
    fig, ax = plt.subplots(figsize=(10, 6))
    n_layers = len(layer_sizes)
    max_neurons = max(layer_sizes)
    x_spacing = 1.0 / (n_layers + 1)
    node_radius = 0.02

    positions = []
    for i, size in enumerate(layer_sizes):
        layer_x = (i + 1) * x_spacing
        layer_positions = []
        y_offset = 0.5
        y_spacing = 0.8 / (size + 1) if size > 1 else 0
        for j in range(size):
            y = y_offset - 0.4 + (j + 1) * y_spacing if size > 1 else y_offset
            layer_positions.append((layer_x, y))
        positions.append(layer_positions)

    for i in range(n_layers - 1):
        for x1, y1 in positions[i]:
            for x2, y2 in positions[i + 1]:
                ax.plot([x1, x2], [y1, y2], "grey", linewidth=0.5, alpha=0.5)

    layer_labels = (
        ["Input"]
        + [f"Hidden {i}" for i in range(1, n_layers - 1)]
        + ["Output"]
    )
    colors = ["#60a5fa"] + ["#a78bfa"] * (n_layers - 2) + ["#f87171"]
    for i, (layer_pos, color) in enumerate(zip(positions, colors)):
        for x, y in layer_pos:
            circle = plt.Circle((x, y), node_radius, color=color, ec="k", lw=0.8, zorder=3)
            ax.add_patch(circle)
        mid_y = np.mean([p[1] for p in layer_pos])
        ax.text(
            layer_pos[0][0], -0.02, f"{layer_labels[i]}\n({layer_sizes[i]})",
            ha="center", va="top", fontsize=9,
        )

    ax.set_xlim(0, 1)
    ax.set_ylim(-0.1, 1.0)
    ax.set_aspect("equal")
    ax.set_title(title)
    ax.axis("off")
    fig.tight_layout()
    return fig


