def analyze_training(history, threshold=0.01):
    val_losses = history["val_loss"]

    best_idx = val_losses.index(min(val_losses))
    best_epoch = best_idx + 1

    overfit_epoch = None
    for i in range(1, len(val_losses)):
        if val_losses[i] > val_losses[i - 1]:
            overfit_epoch = i + 1
            break

    first_loss = val_losses[0]
    best_loss = val_losses[best_idx]
    improvement = ((first_loss - best_loss) / first_loss) * 100

    converged = False
    if len(val_losses) >= 3:
        last_3 = val_losses[-3:]
        max_diff = max(last_3) - min(last_3)
        converged = max_diff < threshold

    return {
        "best_epoch": best_epoch,
        "overfit_epoch": overfit_epoch,
        "final_improvement": round(improvement, 1),
        "converged": converged,
    }


