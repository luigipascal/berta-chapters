STOP_WORDS = {"the", "a", "an", "is", "are", "was", "were", "in", "on", "at",
              "to", "for", "of", "and", "or", "but", "not", "with", "this",
              "that", "it", "be", "as", "by", "from", "has", "have", "had"}


