# Exercise 5: Model Registry
# =============================================================================
class ModelRegistry:
    def __init__(self):
        self.models = []

    def register(self, name, version, metrics):
        self.models.append({
            "name": name,
            "version": version,
            "metrics": metrics,
        })

    def get_best(self, metric="accuracy", higher_is_better=True):
        if not self.models:
            return None
        key = lambda m: m["metrics"].get(metric, 0)
        return max(self.models, key=key) if higher_is_better else min(self.models, key=key)

    def list_versions(self, name):
        return [m for m in self.models if m["name"] == name]

    def compare(self, name_a, name_b):
        a = [m for m in self.models if m["name"] == name_a]
        b = [m for m in self.models if m["name"] == name_b]
        best_a = max(a, key=lambda m: m["metrics"].get("accuracy", 0)) if a else None
        best_b = max(b, key=lambda m: m["metrics"].get("accuracy", 0)) if b else None
        return {"a": best_a, "b": best_b}

    def __str__(self):
        return (f"ModelRegistry({len(self.models)} models, "
                f"{len(set(m['name'] for m in self.models))} unique)")


