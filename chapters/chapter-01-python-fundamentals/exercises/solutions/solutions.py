# Exercise 2: Batch Processor
# =============================================================================
def batch_stats(numbers, batch_size=3):
    results = []
    for i in range(0, len(numbers), batch_size):
        batch = numbers[i:i + batch_size]
        results.append({
            "batch": i // batch_size,
            "min": min(batch),
            "max": max(batch),
            "mean": sum(batch) / len(batch),
        })
    return results


