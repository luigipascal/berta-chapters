# Exercise 3: Text Tokenizer
# =============================================================================
def tokenize(text):
    words = text.lower().split()
    tokens = []
    for word in words:
        clean = word.strip(".,!?;:'\"()[]{}—-")
        if clean and clean not in STOP_WORDS:
            tokens.append(clean)
    vocabulary = set(tokens)
    return tokens, vocabulary


