# Exercise 1: Data Type Detective
# =============================================================================
def type_detective(value):
    if value is None:
        return "NoneType"
    if isinstance(value, bool):
        return f"boolean, {value}"
    if isinstance(value, int):
        sign = "positive" if value > 0 else "negative" if value < 0 else "zero"
        parity = "even" if value % 2 == 0 else "odd"
        return f"integer, {sign}, {parity}"
    if isinstance(value, float):
        sign = "positive" if value > 0 else "negative" if value < 0 else "zero"
        return f"float, {sign}"
    if isinstance(value, str):
        case = "lowercase" if value.islower() else "uppercase" if value.isupper() else "mixed"
        return f"string, length={len(value)}, {case}"
    if isinstance(value, list):
        return f"list, length={len(value)}"
    if isinstance(value, dict):
        return f"dict, keys={len(value)}"
    if isinstance(value, tuple):
        return f"tuple, length={len(value)}"
    if isinstance(value, set):
        return f"set, size={len(value)}"
    return f"{type(value).__name__}"


