# Exercise 6: Data Pipeline Builder
# =============================================================================
class Pipeline:
    def __init__(self):
        self.steps = []

    def add_step(self, name, func):
        self.steps.append((name, func))
        return self

    def run(self, data):
        result = data
        for name, func in self.steps:
            result = func(result)
        return result

    def describe(self):
        names = [name for name, _ in self.steps]
        print(" -> ".join(names))


